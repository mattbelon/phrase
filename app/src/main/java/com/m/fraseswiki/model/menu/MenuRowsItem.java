package com.m.fraseswiki.model.menu;

public class MenuRowsItem {
	private String descripcion;
	private String tema;
	private String obra;
	private int id;

	public String getDescripcion(){
		return descripcion;
	}

	public String getTema(){
		return tema;
	}

	public int getId(){
		return id;
	}

	public String getAutor(){
		return obra;
	}

}
